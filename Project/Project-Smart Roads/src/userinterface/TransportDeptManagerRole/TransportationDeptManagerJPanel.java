    private void assignJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_assignJButtonActionPerformed

        int selectedRow = tdWorkRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null,"Please Select a Row" );
            return;
        }

        WorkRequest request = (WorkRequest)tdWorkRequestJTable.getValueAt(selectedRow, 0);
        request.setStatus("completed");
        Date date = new Date();
        request.setResolveDate(date);
        JOptionPane.showMessageDialog(null,"Sensor Admin Acknowledged");
        populateTable();
    }//GEN-LAST:event_assignJButtonActionPerformed

