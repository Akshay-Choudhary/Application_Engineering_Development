    public void populateTable()
    {
         DefaultTableModel model = (DefaultTableModel)tdWorkRequestJTable.getModel();
        
        model.setRowCount(0);
        for(WorkRequest request : transportationOrganisation.getWorkQueue().getWorkRequestList())
        {
            Object[] row = new  Object[5];
            row[0] = request;
            row[1] = request.getStatus();
            row[2] = request.getRequestDate();
            row[3] = request.getResolveDate();
            row[4] = request.getDriverAcknowledgement();
            
            model.addRow(row);
            
        }
            
    }

